    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidhabitacion.getText().equals("")) {
            int confirmacion=JOptionPane.showConfirmDialog(rootPane, "¿esta seguro de eliminar?","Confirmar",2);
            
            if (confirmacion==0) {
                fhabitacion func=new fhabitacion();
                dhabitacion dts=new dhabitacion();
                
                dts.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
            }
            
        }
    }//GEN-LAST:event_btneliminarActionPerformed

    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtnumero.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese Número de habitación");
            txtnumero.requestFocus();
            return;         
        }
        if (txtdescripcion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese una descripcion de la habitación");
            txtdescripcion.requestFocus();
            return;         
        }
        if (txtpreciodiario.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese el precio diario de la habitación");
            txtpreciodiario.requestFocus();
            return;         
        }
        if (txtcaracteristicas.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese caracteristicas de la habitación");
            txtcaracteristicas.requestFocus();
            return;         
        }
        
        dhabitacion dts=new dhabitacion();
        fhabitacion func=new fhabitacion();
        
        dts.setNumero(txtnumero.getText());
        
        int seleccionado=cbxpiso.getSelectedIndex();
        dts.setPiso((String)cbxpiso.getItemAt(seleccionado));
        dts.setDescripcion(txtdescripcion.getText());
        dts.setCaracteristicas(txtcaracteristicas.getText());
        
        dts.setPrecio_diario(Double.parseDouble(txtpreciodiario.getText()));
        
        seleccionado = cbxestado.getSelectedIndex();
        dts.setEstado((String) cbxestado.getItemAt(seleccionado));
        
        seleccionado = cbxtipo_habitacion.getSelectedIndex();
        dts.setTipo_habitacion((String) cbxtipo_habitacion.getItemAt(seleccionado));
        
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "Habitacion insertada");
                // y para que muestre todo los registros con una cadena vacia
                mostrar("");
                inhabilitar();
                
                
            }
            
        } else if (accion.equals("editar")) {
            dts.setIdhabitacion(Integer.parseInt(txtidhabitacion.getText()));
            
            if (func.editar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "Habitacion Editada e insertada");
                // y para que muestre todo los registros con una cadena vacia
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

